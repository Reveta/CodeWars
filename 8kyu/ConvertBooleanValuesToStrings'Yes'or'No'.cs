namespace CodeWars._8kyu {
	public class ConvertBooleanValuesToStrings_Yes_or_No_ {
		/**
		 * https://www.codewars.com/kata/53369039d7ab3ac506000467/train/csharp
		 * Complete the method that takes a boolean value
		 * and return a "Yes" string for true, or a "No" string for false.
		 */
		public static string boolToWord(bool word) {
			return word ? "Yes" : "No";
		}
	}
}